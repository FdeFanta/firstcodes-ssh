
public class SubString {
	public static void main(String[] args){
		String letters = "abcdefghijklmabcdefghijklm";
		
		//metodo substring
		
		System.out.printf(
				"Substring do index 20 até o final é \"%s\"%n", letters.substring(20));
		System.out.printf("%s\"%s\"%n",
				"Substring do index 3 até o 6 é ", letters.substring(3,6));
		
		
		}

}
